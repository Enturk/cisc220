#include <iostream>
#include <cassert>
#include <bitset>
using namespace std;

//Edit these two functions
void set_bit(unsigned char &x, int i, bool value){
    //set bit i to value (in place)
    if (value) x |= 1 << i;
    else x &= ~(1 << i);
}

bool get_bit(unsigned char x, int i){
    //return the value of bit i
    return (x >> i) & 1;

}
//stop editing here

int main() {
    unsigned char x = 0;
    set_bit(x, 3, 1);
    set_bit(x, 5, 1);
    set_bit(x, 7, 1);
    
    assert(x % 2 == 0);
    assert(!get_bit(x,1));
    assert(!get_bit(x,2));
    assert(get_bit(x,3));
    assert(!get_bit(x,4));
    assert(get_bit(x,5));
    assert(!get_bit(x,6));
    assert(get_bit(x,7));
    
    set_bit(x, 3, 0);
    set_bit(x, 5, 0);
    assert(!get_bit(x, 0));
    assert(!get_bit(x,1));
    assert(!get_bit(x,2));
    assert(!get_bit(x,3));
    assert(!get_bit(x,4));
    assert(!get_bit(x,5));
    assert(!get_bit(x,6));
    assert(get_bit(x,7));
    
    cout << "All tests pass" << endl;
}